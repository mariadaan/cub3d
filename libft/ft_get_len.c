int			ft_get_len(int n)
{
	int len;

	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		len++;
	while (n)
	{
		if (n > 0)
			n = n / 10;
		else
			n = n / -10;
		len++;
	}
	return (len);
}
